-- =====================================================
-- 修复 download_count 更新权限问题
-- 创建时间: 2025-11-11
-- 描述: 允许所有用户（包括未登录用户）更新 download_count 字段
-- =====================================================

-- =====================================================
-- 1. 删除旧的更新策略
-- =====================================================

DROP POLICY IF EXISTS "Only admins can update templates" ON global_templates;

-- =====================================================
-- 2. 创建新的更新策略（简化版）
-- =====================================================

-- 策略：所有人可以更新 download_count（包括未登录用户）
CREATE POLICY "Anyone can update download count"
  ON global_templates
  FOR UPDATE
  USING (true);

